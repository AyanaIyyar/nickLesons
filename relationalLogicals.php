//Relational Operators
/**
 * They are used for comparing different conditions.
 * They include :
 * == - Equal To
 * < - Less Than
 * > - Greater Than
 * <= - Less Than or Equal To
 * >= - Greater Than or Equal To
 * != - Not Equal To
 */
//Logical Operators
/**
 * They perform logical operations of data.
 * They include :
 * a) && - Logical AND - Returns a TRUE result if all conditions are TRUE.
 * b) || - Logical OR - Returns a TRUE result if either one or several or
 * all conditions are TRUE.
 * c) ! - Logical NOT - Returns an inverse of a result, that is, if a result
 * is TRUE, it returns FALSE and vice versa.
 */