<?php
//Variables
/**
 * A Variable is a temporary storage location for data in the computer's
 * memory.
 */
//Declaring and Initializing Variables
$num1 = 23; //Integer Variable
$num2 = 12.345; //Decimal Variable
$num3 = 1234567.890; //Double Variable
$letter = 'A'; //Single Character Variable
$full_names = "Jack Mwangi"; //String / Long Text Variable
//Results / Output
echo "<b>Integer Value : </b>" . $num1 . "<br>";
echo "<b>Decimal Value : </b>" . $num2 . "<br>";
echo "<b>Double Value  : </b>" . $num3 . "<br>";
echo "<b>First Letter  : </b>" . $letter . "<br>";
echo "<b>Full Names : </b>" . $full_names . "<br>";
//Rules to follow when naming variables
/**
 * 1. A variable name must start with a letter.
 * 2. When spacing variable names, use an underscore (_)
 * 3. Variable names should not be reserved keywords
 * 4. Variable names are case-sensitive
 */