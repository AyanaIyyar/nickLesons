<?php
//Functions
/**
 * A function is a block of code that performs a particular task.
 *
 * Syntax
 * function functionName(arguments)
 * {
 *      some code
 * }
 */
//Example One - A function code with no arguments
function displayText(){
    print "I am a function that has no arguments";
}
//Function Calling
/**
 * To display content of a function, it must be called.
 *
 * Syntax
 * functionName();
 */
displayText();