<?php
//Increase and Decrease Operators
/**
 * The increase operator (++) adds a value by 1; the decrease operator
 * (--) reduces a value by 1.
 */
//Declaring and initializing variables for computations
$num1=23;
$num2=12;
//Using increase and decrease operators to perform computations
$num1++;
$num2--;
print "<b>Result One : </b>" . $num1 . "<br>";
print "<b>Result Two : </b>" . $num2 . "<br>";